MatrixToFuzzyNumbers <- function(data, varNames=NA)
{

  output <- list(rep(NA,nrow(data)))

  if(ncol(data)==3)
  {
    for (i in 1:nrow(data)) {

      output[[i]] <- TriangularFuzzyNumber(a1=data[i,1],amid = data[i,2],a4 = data[i,3])


    }


  }

  if(ncol(data)==4)
  {
    for (i in 1:nrow(data)) {

      output[[i]] <- TrapezoidalFuzzyNumber(a1=data[i,1],a2 = data[i,2],a3 = data[i,3],a4 = data[i,4])


    }

  }

  if(!anyNA(varNames))
  {
    names(output) <- varNames
  }

  return(output)

}


